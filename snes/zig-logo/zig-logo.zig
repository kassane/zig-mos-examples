// SNES Zig-mark logo with shimmer palette animation.
// Mode 0 BG1, 8×8 tiles, logo anchored at tile column 8, row 7.
// Tilemap at VRAM word 0x0000 (BG1SC=0x00), CHR at word 0x1000 (BG12NBA=0x01, unit=0x1000 words).

pub const panic = @import("mos_panic");

const hw = @import("snes");
const sneslib = @import("sneslib");
comptime {
    _ = @import("snes_header");
}

const TILE_COUNT = 0x47; // tiles 0x00–0x46 used by the logo

const nes_chr = @embedFile("zig-mark.chr");

/// Comptime NES 2bpp → SNES 2bpp conversion.
/// NES layout per tile: [p0r0..p0r7, p1r0..p1r7]
/// SNES layout per tile: [p0r0, p1r0, p0r1, p1r1, ..., p0r7, p1r7]
const snes_chr: [TILE_COUNT * 16]u8 = blk: {
    var out: [TILE_COUNT * 16]u8 = undefined;
    for (0..TILE_COUNT) |t| {
        for (0..8) |row| {
            out[t * 16 + row * 2 + 0] = nes_chr[t * 16 + row];
            out[t * 16 + row * 2 + 1] = nes_chr[t * 16 + 8 + row];
        }
    }
    break :blk out;
};

const logo_col: u8 = 8;
const logo_row: u8 = 7;
